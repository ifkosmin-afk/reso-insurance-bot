#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# Запуск Telegram-бота РЕСО-Гарантия для страховых агентов
# ─────────────────────────────────────────────────────────────────────────────

BOT_DIR="$(cd "$(dirname "$0")" && pwd)"
INDEX="$BOT_DIR/reso_bm25.pkl"

# Проверка токена
if [ -z "$TELEGRAM_TOKEN" ]; then
  echo "❌ Ошибка: переменная TELEGRAM_TOKEN не установлена."
  echo "   Запустите: export TELEGRAM_TOKEN=ваш_токен"
  exit 1
fi

# Пересборка индекса если нужно
if [ ! -f "$INDEX" ]; then
  echo "📚 Индекс не найден. Строю индекс..."
  python3 "$BOT_DIR/build_index_bm25.py"
fi

echo "🤖 Запускаю бота РЕСО-Гарантия..."
python3 "$BOT_DIR/bot.py"
